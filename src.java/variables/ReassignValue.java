package variables;

public class ReassignValue {
    public static void main(String[] args) {
        char myName = 'p';
        myName = 'y';
        System.out.println(myName);
        byte myValue1 = 124;
        myValue1 = 23;
        System.out.println(myValue1);
        short myCode = 4567;
        myCode = 2345;
        System.out.println(myCode);
        int myPassword = 234558978;
        myPassword = 4533422;
        System.out.println(myPassword);
        long myNumber = 173187391894L;
        myNumber = 3761868164817L;
        System.out.println(myNumber);
        float myValue2 = 23.45f;
        myValue2 = 34.2f;
        System.out.println(myValue2);
        double myValue3 = 1234.3764;
        myValue3 = 2389.83;
        System.out.println(myValue3);
        boolean myAnswer = true;
        myAnswer = false;
        System.out.println(myAnswer);

    }
}
