package operator;

public class ComparisonOperators {

    public static void main(String[] args) {

        int a = 30;
        int b = 40;
        int c = 50;

        System.out.println(a==b);
        System.out.println(b!=c);
        System.out.println(c>a);
        System.out.println(a<b);
        System.out.println(b<=a);
        System.out.println(c>=b);

    }
}
