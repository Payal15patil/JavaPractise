package operator;

public class ArithmeticOperators {

    public static void main(String[] args) {
        int p = 100;
        int q = 7;
        int r = 45;

        System.out.println(p+q+r+p+q+r);
        System.out.println(r-q-p-r-q-p);
        System.out.println(p*q*r);
        System.out.println(r/p);
        System.out.println(p%q);
    }
}
